public class BasicBuffer {

    public static void main(String[] args) {
        //分配
        IntBuffer intBuffer = IntBuffer.allocate(5);

        for(int i=0;i<intBuffer.capacity();i++){
            intBuffer.put(i*2);
        }
        //如何从 buffer 读取数据
        //将 buffer 转换，读写切换(!!!)
        intBuffer.flip();
        while (intBuffer.hasRemaining()){
            System.out.println(intBuffer.get());
        }

    }
}

